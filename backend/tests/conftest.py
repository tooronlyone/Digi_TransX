@pytest.fixture(scope="session")
def pg_connection():
    test_url = os.environ.get(TEST_DB_ENV, "").strip()
    if not test_url:
        pytest.skip(SKIP_REASON)
    if test_url == os.environ.get("SUPABASE_DB_URL", "").strip():
        pytest.skip(
            "TEST_SUPABASE_DB_URL matches SUPABASE_DB_URL; integration tests "
            "require a dedicated test database, not the application database."
        )

    import psycopg2

    conn = psycopg2.connect(test_url)
    schema = f"dtx_test_{uuid.uuid4().hex[:12]}"
    # Tests that open extra connections (genuine concurrency) look up the
    # session's isolated schema + URL here (psycopg2 connections don't accept
    # arbitrary attributes).
    TEST_SESSION_INFO["schema"] = schema
    TEST_SESSION_INFO["url"] = test_url
    try:
        with conn.cursor() as cur:
            cur.execute(f'create schema "{schema}"')
            cur.execute(f'set search_path to "{schema}"')
            cur.execute(DDL)
        conn.commit()
        yield conn
    finally:
        conn.rollback()
        with conn.cursor() as cur:
            cur.execute(f'drop schema "{schema}" cascade')
        conn.commit()
        conn.close()


@pytest.fixture
def pg_session_info(pg_connection):
    """The isolated schema + URL, for tests that open their own connections
    (e.g. genuine concurrency and route-level tests). Exposed as a fixture so
    callers never re-import conftest as a second module."""
    return {"schema": TEST_SESSION_INFO["schema"], "url": TEST_SESSION_INFO["url"]}


@pytest.fixture
def db(pg_connection):
    from shared.db import Db

    yield Db(pg_connection)
    # A test may end mid-transaction (e.g. after asserting a constraint
    # violation); roll back first, then reset the tables for the next test.
    pg_connection.rollback()
    with pg_connection.cursor() as cur:
        cur.execute(
            "truncate terms_acknowledgements, terms_versions, commission_policies, "
            "payments, shipment_no_show_tracking, shipment_trips, shipment_bids, shipments, "
            "wallet_transactions, wallets, user_payment_preferences, saved_payment_methods, "
            "transporter_profiles "
            "restart identity cascade"
        )
    pg_connection.commit()


