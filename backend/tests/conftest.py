"""Test fixtures for the commission-policy suite.

Two tiers of tests:

- Pure calculation/validation tests run with no database at all.
- Database-backed tests are PostgreSQL integration tests. They require a
  DEDICATED test database configured via the TEST_SUPABASE_DB_URL environment
  variable. There is deliberately no fallback to SUPABASE_DB_URL: tests must
  never touch the production database. When TEST_SUPABASE_DB_URL is not set,
  the integration tests are skipped with a clear reason.

Isolation: each test session creates a uniquely named schema on the test
database, installs the commission tables there (mirroring
supabase/schema.sql, including CHECK constraints and the immutability
trigger), pins the connection's search_path to that schema, and drops the
schema again afterwards. Between tests the tables are truncated. The tests
reuse the production Db wrapper from shared.db — there is no second
database abstraction.
"""

