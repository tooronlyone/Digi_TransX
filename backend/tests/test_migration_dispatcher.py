def test_canonical_schema_is_dispatcher_free_and_migration_is_noop(make_db):
    """A fresh load of the real schema.sql already has no dispatcher, and the
    removal migration applied on top is a clean idempotent no-op across the
    complete policy set."""
    if not SCHEMA_SQL.exists():   # pragma: no cover
        pytest.skip("schema.sql not found")
    conn = make_db(STUBS + SCHEMA_SQL.read_text(encoding="utf-8"))
    with conn.cursor() as cur:
        assert "dispatcher" not in _enum_labels(cur)
        assert _scalar(cur, "select count(*) from pg_proc p join pg_namespace n "
                            "on n.oid=p.pronamespace where n.nspname='public' "
                            "and p.proname='is_dispatcher'") == 0
        assert _scalar(cur, f"select count(*) from pg_policies where "
                            f"schemaname in ('public','storage') and "
                            f"({DISPATCHER_POLICY_FILTER})") == 0
        before = _policy_rows(cur)
        cur.execute(_migration_sql())     # must be a clean no-op
        conn.commit()
        assert _policy_rows(cur) == before
        assert "dispatcher" not in _enum_labels(cur)
