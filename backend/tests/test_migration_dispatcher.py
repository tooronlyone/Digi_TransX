"""Tests for the dispatcher-role removal (migration 20260723110000).

Two independent construction paths, per the task:

  * PRE-MIGRATION FIXTURE (`PRELUDE`) — a self-contained, dispatcher-bearing
    database that faithfully reproduces every object migration 20260723110000
    touches (the app_role enum WITH dispatcher, is_dispatcher(), the signup
    trigger's dispatcher mapping, all dispatcher RLS/storage policies, and the
    neighbouring admin/owner/transporter/client/seeker/everyday policies). The
    migration is applied to it and the end state is asserted. This mirrors the
    repo's existing migration-test convention (test_migration_everyday's
    PRELUDE, test_profile_rls's SETUP) and stays valid forever — it does not
    depend on git history that moves once this branch merges.

  * FULL CANONICAL SCHEMA — the real supabase/schema.sql (the head schema,
    i.e. the state a database reaches after every migration through
    20260723100000). A fresh load must already be dispatcher-free (a new
    deployment never gets a dispatcher), and applying the removal migration on
    top must be a clean no-op (idempotency on the real, complete policy set).

Pure-calculation tests (no database) cover the application layer: dispatcher is
neither a legacy mapping nor an accepted signup role.

The database tests need a server the test user can CREATE DATABASE on. They are
SKIPPED (never failed) when that is not permitted (e.g. a locked-down managed
Supabase) or when TEST_SUPABASE_DB_URL is unset — exactly like the other
migration tests in this suite.
"""

