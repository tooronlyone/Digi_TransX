def test_migration_aborts_if_dispatcher_user_exists(make_db):
    """req 12: a dispatcher user makes the migration abort and change nothing."""
    import psycopg2
    conn = make_db(PRELUDE)
    with conn.cursor() as cur:
        cur.execute("insert into public.users (email, cnic, role, legacy_role) "
                    "values ('disp@test','1112223334445','dispatcher','dispatcher')")
        conn.commit()
        with pytest.raises(psycopg2.errors.RaiseException):
            cur.execute(_migration_sql())
        conn.rollback()
        # Nothing changed: dispatcher artifacts all still present.
        assert "dispatcher" in _enum_labels(cur)
        assert _scalar(cur, "select count(*) from pg_proc p join pg_namespace n "
                            "on n.oid=p.pronamespace where n.nspname='public' "
                            "and p.proname='is_dispatcher'") == 1
        assert _scalar(cur, "select count(*) from public.users where role::text='dispatcher'") == 1
        assert _scalar(cur, "select count(*) from pg_policies where policyname='drivers_dispatcher_all'") == 1


def test_migration_deletes_no_rows(make_db):
    """req 13: a normal zero-dispatcher migration deletes no user/business rows."""
    conn = make_db(PRELUDE)
    with conn.cursor() as cur:
        cur.execute("""
            insert into public.users (email,cnic,role,legacy_role) values
              ('admin@test','1000000000001','admin','platform_admin'),
              ('seeker@test','1000000000002','customer','service_seeker'),
              ('eve@test','1000000000003','customer','everyday_user'),
              ('carrier@test','1000000000004','transporter','transporter');
            insert into public.service_seeker_profiles (user_id, company_name)
              select id, 'BizCo' from public.users where email='seeker@test';
            insert into public.everyday_user_profiles (user_id)
              select id from public.users where email='eve@test';
            insert into public.shipments (client_user_id, status)
              select id, 'open' from public.users where email='seeker@test';
        """)
        conn.commit()
        before = {t: _scalar(cur, f"select count(*) from public.{t}") for t in
                  ("users", "service_seeker_profiles", "everyday_user_profiles", "shipments")}
        cur.execute(_migration_sql())
        conn.commit()
        after = {t: _scalar(cur, f"select count(*) from public.{t}") for t in before}
        assert after == before
        # The seeker/everyday split is intact and non-overlapping.
        assert _scalar(cur, "select count(*) from public.service_seeker_profiles s "
                            "join public.everyday_user_profiles e on e.user_id=s.user_id") == 0


def test_migration_is_idempotent(make_db):
    """req 15: re-applying the migration is a safe no-op."""
    conn = make_db(PRELUDE)
    with conn.cursor() as cur:
        cur.execute(_migration_sql())
        conn.commit()
        first = _policy_rows(cur)
        first_enum = _enum_labels(cur)
        cur.execute(_migration_sql())     # re-apply
        conn.commit()
        assert _policy_rows(cur) == first
        assert _enum_labels(cur) == first_enum
        assert "dispatcher" not in first_enum


