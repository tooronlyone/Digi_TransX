def _admin_url():
    url = os.environ.get("TEST_SUPABASE_DB_URL", "").strip()
    if not url:
        pytest.skip("TEST_SUPABASE_DB_URL not set")
    parts = urlsplit(url)
    return url, parts, urlunsplit((parts.scheme, parts.netloc, "/postgres", "", ""))


@pytest.fixture
def make_db():
    """Factory: build a fresh throwaway database from a SQL string and return a
    live connection to it. Every database created is dropped at teardown."""
    psycopg2 = pytest.importorskip("psycopg2")
    _, parts, admin_url = _admin_url()
    try:
        admin = psycopg2.connect(admin_url)
    except Exception as exc:  # pragma: no cover
        pytest.skip(f"cannot reach admin database: {exc}")
    admin.autocommit = True
    created = []
    conns = []

    def _factory(setup_sql):
        dbname = f"dtx_disp_{uuid.uuid4().hex[:12]}"
        try:
            with admin.cursor() as cur:
                cur.execute(f"CREATE DATABASE {dbname}")
        except Exception as exc:
            pytest.skip(f"CREATE DATABASE not permitted: {exc}")
        created.append(dbname)
        conn = psycopg2.connect(urlunsplit((parts.scheme, parts.netloc, "/" + dbname, "", "")))
        conns.append(conn)
        with conn.cursor() as cur:
            cur.execute(setup_sql)
        conn.commit()
        return conn

    try:
        yield _factory
    finally:
        for c in conns:
            try:
                c.close()
            except Exception:
                pass
        with admin.cursor() as cur:
            for dbname in created:
                cur.execute(f"DROP DATABASE IF EXISTS {dbname}")
        admin.close()


def _migration_sql():
    return MIGRATION_SQL.read_text(encoding="utf-8")


def _scalar(cur, sql):
    cur.execute(sql)
    return cur.fetchone()[0]


def _enum_labels(cur):
    cur.execute("select enumlabel from pg_enum e join pg_type t on t.oid=e.enumtypid "
                "join pg_namespace n on n.oid=t.typnamespace "
                "where n.nspname='public' and t.typname='app_role' order by e.enumsortorder")
    return [r[0] for r in cur.fetchall()]


def _policy_rows(cur):
    cur.execute("select schemaname, tablename, policyname, cmd, coalesce(qual,''), "
                "coalesce(with_check,'') from pg_policies "
                "where schemaname in ('public','storage') order by 1,2,3")
    return set(cur.fetchall())


def test_migration_removes_all_dispatcher_artifacts(make_db):
    """req 4,5,6: after migration — no dispatcher enum value, no is_dispatcher()
    function, no dispatcher policy in public or storage, no temp enum left."""
    conn = make_db(PRELUDE)
    with conn.cursor() as cur:
        assert "dispatcher" in _enum_labels(cur)                       # precondition
        cur.execute(_migration_sql())
        conn.commit()

        assert _enum_labels(cur) == ["admin", "customer", "transporter",
                                     "fuel_station_manager", "shopkeeper"]
        assert "dispatcher" not in _enum_labels(cur)                   # req 4
        assert _scalar(cur, "select count(*) from pg_proc p join pg_namespace n "
                            "on n.oid=p.pronamespace where n.nspname='public' "
                            "and p.proname='is_dispatcher'") == 0       # req 5
        assert _scalar(cur, f"select count(*) from pg_policies where "
                            f"schemaname in ('public','storage') and "
                            f"({DISPATCHER_POLICY_FILTER})") == 0       # req 6
        assert _scalar(cur, "select count(*) from pg_type t join pg_namespace n "
                            "on n.oid=t.typnamespace where n.nspname='public' "
                            "and t.typname like 'app_role__%'") == 0    # no temp type


def test_non_dispatcher_policies_survive_enum_rebuild(make_db):
    """req 7-11,14: every non-dispatcher policy is byte-identical before/after;
    the only removals are the dispatcher policies + the users_select_own
    rewrite, and the only addition is the dispatcher-free users_select_own."""
    conn = make_db(PRELUDE)
    with conn.cursor() as cur:
        before = _policy_rows(cur)
        cur.execute(_migration_sql())
        conn.commit()
        after = _policy_rows(cur)

        removed = before - after
        added = after - before

        # Everything removed is a dispatcher artifact (either a dispatcher
        # policy, or the old users_select_own that referenced is_dispatcher()).
        for row in removed:
            _, _, name, _, qual, chk = row
            assert ("dispatcher" in name or "is_dispatcher" in qual
                    or "is_dispatcher" in chk), f"unexpected removal: {row}"
        # The only thing added back is the rewritten users_select_own.
        assert {(r[1], r[2]) for r in added} == {("users", "users_select_own")}

        # Every role's defining policy is still present.
        surviving = {r[2] for r in after}
        for name in PRESERVED_POLICIES:
            assert name in surviving, f"lost non-dispatcher policy: {name}"

        # And none of the surviving non-dispatcher policies changed definition.
        nondisp_before = {r for r in before
                          if "dispatcher" not in r[2]
                          and "is_dispatcher" not in r[4]
                          and "is_dispatcher" not in r[5]
                          and r[2] != "users_select_own"}
        assert nondisp_before <= after                                # req 14


